export const getRateFromValue = (value: string | undefined): PerDiemData => {
    if (!value) return PER_DIEM_DATA.find(r => r.country === 'Germany')!;

    if (value.includes('|')) {
        const [country, city] = value.split('|');
        const found = PER_DIEM_DATA.find(r => r.country === country && r.city === city);
        if (found) return found;
    } else {
        // Fallback for names/codes
        const found = PER_DIEM_DATA.find(r => r.country === value && r.city === 'Standard');
        if (found) return found;

        // Check for Germany special case (Inland)
        if (value === 'Germany' || value === 'DE') {
            return PER_DIEM_DATA.find(r => r.country === 'Germany')!;
        }

        // Legacy Code mappings (minimal set for tests/compatibility)
        if (value === 'NO') return PER_DIEM_DATA.find(r => r.country === 'Norway')!;
        if (value === 'US') return PER_DIEM_DATA.find(r => r.country === 'USA')!;
        if (value === 'KR') return PER_DIEM_DATA.find(r => r.country === 'South Korea')!;

        // Direct matches by country matching name (without city)
        const directMatch = PER_DIEM_DATA.find(r => r.country === value);
        if (directMatch) return directMatch;
    }

    // Default
    const defaultRate = PER_DIEM_DATA.find(r => r.country === 'Germany');
    return defaultRate || PER_DIEM_DATA[0];
};

export const getCountryOptions = () => {
    return PER_DIEM_DATA.map(r => ({
        value: `${r.country}|${r.city}`,
        label: r.city !== 'Standard' && r.city !== 'Inland'
            ? `${r.country} - ${r.city}`
            : r.country,
        fullDay: r.fullDay,
        partialDay: r.partialDay,
    })).sort((a, b) => a.label.localeCompare(b.label));
};
